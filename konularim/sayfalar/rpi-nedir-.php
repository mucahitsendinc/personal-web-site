<?php include'header.php'; ?><div class="col-12"></head><h1>Raspberry Pi kısaca Nedir ? </h1><img class="img-fluid" src="https://i.hizliresim.com/DGXGav.jpg"><p>Yukarıda gördüğünüz resimdeki bir avuç hatta bir kredi kartı boyutundaki işlemci benzeri cihazın adı RPi yani Raspberry Pi dır.Bu cihaza mini bilgisayar dersek de abartmış olmayız hatta az söylemiş bile olabiliriz. Raspberry Pi'ın kendi işlemci,rami,hafızası vs. bir bilgisayarda olan hemen hemen herşey vardır. Hatta şöyle de söyleyebiliriz Raspberry Pi da bilgisayarda olan çoğu şey var fakat bilgisayarda RPi deki herşey yok arkadaşlar.Üzerindeki HDMI girişi ile monitör bağlayabilir usb portları ile klavye mouse takabilirsiniz. Cebinize koyabileceğiniz bir bilgisayardır RPi.</p> 
<h2>Ne işe yarar bu RPi?</h2>
<p>Üzerindeki 40 adet pin girişleri sayesinde çeşitli sensörlerden değer alıp vermeye , işimizi kolaylaştıracak cihazlara dönüştürmeye yarar RPi. Üzerindeki 40 adet pin çıkıntılarınını GPIO,GND,5v ve 3v çıkışları oluşturur. Bu çıkışların ne işe yaradığına gelirsek.
<ul>
  <li><b>GPIO : </b>Sensörlerden değer okuma , gönderme işlemlerimizi gerçekleştirir yeri geldiğinde aldığımız değere göre güç de gönderebiliriz bu pinden.</li>
  <li><b>GND : </b>Bu pin ise (-) yani negatif güç vermemizi sağlar</li>
  <li><b>5v & 3v :</b> Bu pin girişleri ise RPi den dışarıya güç aktarmamızı sağlarlar şemalarda VCC olarak ta görebiliriz.</li>
</ul>
</p>
<h2>Peki ya Kodlar?</h2>
<p>RPi cihazımızın kendi işletim sistemi bulunmakla beraber farklı işletim sistemleri de kurabiliyorsunuz. Demiştik ya o da bir bilgisayar. Robotik kodlama için Rasbian kurduğumuz bu cihazımızda kodları istediğimiz ide yi kurup yazabilir ve çalıştırabiliriz.</p>
